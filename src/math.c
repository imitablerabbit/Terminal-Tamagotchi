#include "termpet.h"

float max_float(float x, float y) {
    if (x > y)
        return x;
    return y;
}

float min_float(float x, float y) {
    if (x < y)
        return x;
    return y;
}

int max_int(int x, int y) {
    if (x > y)
        return x;
    return y;
}

int min_int(int x, int y) {
    if (x < y)
        return x;
    return y;
}
